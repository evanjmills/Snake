package Gui;

public class DirectionCordinate {
	private String dir;
	private double x;
	private double y;
	
	public DirectionCordinate(String dir, double x, double y){
		this.dir = dir;
		this.x = x;
		this.y = y;
	}

	public String getDir() {
		return dir;
	}

	public double getX() {
		return x;
	}

	public double getY() {
		return y;
	}
	
	
}
